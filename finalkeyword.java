public class finalkeyword {
    public static void main(String[] args) {
        final int PI;

        PI = 24;
        System.out.println(PI);

        // final variable used to create constants 

        // final class makes other class fail to extend this class 
        // i.e it stops the inheritance of this class by other classes 

        // final method prevents that method being overriden by child class during inheritance 
        
    }
}
